package com.ssm.domain;

public class Job {

    private int job_id;

    private String job_name;

    private String job_place;

    private String job_kind;

    private String job_time;

    private String job_release_time;

    private String job_demand;

    private String job_duty;

    public int getJob_id() {
        return job_id;
    }

    public void setJob_id(int job_id) {
        this.job_id = job_id;
    }

    public String getJob_name() {
        return job_name;
    }

    public void setJob_name(String job_name) {
        this.job_name = job_name;
    }

    public String getJob_place() {
        return job_place;
    }

    public void setJob_place(String job_place) {
        this.job_place = job_place;
    }

    public String getJob_kind() {
        return job_kind;
    }

    public void setJob_kind(String job_kind) {
        this.job_kind = job_kind;
    }

    public String getJob_time() {
        return job_time;
    }

    public void setJob_time(String job_time) {
        this.job_time = job_time;
    }

    public String getJob_release_time() {
        return job_release_time;
    }

    public void setJob_release_time(String job_release_time) {
        this.job_release_time = job_release_time;
    }

    public String getJob_demand() {
        return job_demand;
    }

    public void setJob_demand(String job_demand) {
        this.job_demand = job_demand;
    }

    public String getJob_duty() {
        return job_duty;
    }

    public void setJob_duty(String job_duty) {
        this.job_duty = job_duty;
    }
}
