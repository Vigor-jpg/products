package com.ssm.domain;

public class SampleProduct {

    private int product_id;

    private String product_name;

    private String product_detail;

    private String product_img_path;

    private String product_url;

    private String product_target;

    public int getProduct_id() {
        return product_id;
    }

    public void setProduct_id(int product_id) {
        this.product_id = product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getProduct_detail() {
        return product_detail;
    }

    public void setProduct_detail(String product_detail) {
        this.product_detail = product_detail;
    }

    public String getProduct_img_path() {
        return product_img_path;
    }

    public void setProduct_img_path(String product_img_path) {
        this.product_img_path = product_img_path;
    }

    public String getProduct_url() {
        return product_url;
    }

    public void setProduct_url(String product_url) {
        this.product_url = product_url;
    }

    public String getProduct_target() {
        return product_target;
    }

    public void setProduct_target(String product_targt) {
        this.product_target = product_targt;
    }

}
